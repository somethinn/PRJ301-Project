    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        
        
        
         String showID_raw = request.getParameter("showID");
        String statusID_raw = request.getParameter("statusID");
        int showID, statusID;
        
        try {
            showID = Integer.parseInt(showID_raw);
            statusID = Integer.parseInt(statusID_raw);
            ShowDAO showDAO = new ShowDAO();
            ShowDTO show = showDAO.getShow(showID);
            if(statusID == 1) {
                statusID = 0;
            } else {
                statusID = 1;
            }
            show.setShowStatus(statusID);
            showDAO.updateShowStatus(show);
            response.sendRedirect("show-management");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

